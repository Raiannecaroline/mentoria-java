package exerciciosAula01;

public class MostrarTrueOuFalse {

    /**
     * Crie uma variável do tipo boolean chamada status e atribua o valor true a ela.
     * Em seguida, escreva um programa que imprime na tela o valor dessa variável em formato de texto, ou seja, "true" ou "false".
     */

    boolean b1 = true;

    public void isVariabel() {
        if (b1) {
            System.out.println("É verdadeiro");
        } else {
            System.out.println("É falso");
        }
    }
}
